diem = float(input("Nhap vao so diem ban muon: "))
diem+=20
print(f"Sau khi cong 20: {diem}")
diem-=15
print(f"Sau khi tru 15: {diem}")
diem*=2
print(f"Diem sau khi nhan doi: {diem}" )
diem/=3
print(f"Diem sau khi chia 3: {diem:.2f}")
diem//=2
print(f"Diem sau khi chia 2 (Lay phan nguyen): {diem}")
diem%=5
print(f"Diem sau khi chia du cho 5: {diem}")